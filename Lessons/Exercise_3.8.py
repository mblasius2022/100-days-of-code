places_to_visit = ['accra', 'belize', 'prague', 'budapest', 'canada', 'ethiopia', 'nicaragua']

#print original list
print(places_to_visit)
#print sorted list
print(sorted(places_to_visit))
#print original list to show that not impacted by sorted
print(places_to_visit)
# use reverse
places_to_visit = places_to_visit.reverse()
print(places_to_visit)
#sort list
places_to_visit = ['accra', 'belize', 'prague', 'budapest', 'canada', 'ethiopia', 'nicaragua']
places_to_visit.sort(reverse=True)
